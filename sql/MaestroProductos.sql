-- Función para calcular la cantidad de cajas
CREATE OR REPLACE FUNCTION calculate_box_quantity()
RETURNS TRIGGER AS $$
DECLARE
    product_data RECORD;
    quantity_box INTEGER;
BEGIN
    -- Obtener información del producto
    SELECT p.amount_of_content, p.mt2_box, im.quantity, im.unit_measure_id
    INTO product_data
    FROM products p
    INNER JOIN inventory_movements im ON p.id = im.product_id
    WHERE im.id = NEW.id;

    -- Calcular la cantidad de cajas según la unidad de medida
    IF product_data.unit_measure_id = (SELECT id FROM unit_measure WHERE code = 'PALETA') THEN
        quantity_box := TRUNC(product_data.quantity * product_data.amount_of_content);
    ELSIF product_data.unit_measure_id = (SELECT id FROM unit_measure WHERE code = 'GRANEL') THEN
        IF product_data.mt2_box > 0 THEN
            quantity_box := TRUNC(product_data.quantity / product_data.mt2_box);
        ELSE
            -- Manejar el caso donde mt2_box es cero para evitar división por cero
            RAISE NOTICE 'Advertencia: mt2_box es cero para el producto %. No se puede calcular la cantidad de cajas para GRANEL.', NEW.product_id;
            quantity_box := 0;
        END IF;
    ELSE
        -- Para otras unidades de medida, se asume que la cantidad es en cajas
        quantity_box := TRUNC(product_data.quantity);
    END IF;

    -- Actualizar el inventario
    IF TG_OP = 'INSERT' THEN
        IF NEW.movement_type = 'ENTRADA' THEN
            -- Insertar o actualizar el stock en la tabla inventory
            INSERT INTO inventory (warehouse_id, product_id, stock_quantity, last_update)
            VALUES (NEW.warehouse_id, NEW.product_id, quantity_box, NOW())
            ON CONFLICT (warehouse_id, product_id) DO UPDATE
            SET stock_quantity = inventory.stock_quantity + quantity_box,
                last_update = NOW();
        ELSIF NEW.movement_type = 'SALIDA' THEN
            -- Verificar si hay suficiente stock antes de la salida
            IF EXISTS (SELECT 1 FROM inventory WHERE warehouse_id = NEW.warehouse_id AND product_id = NEW.product_id AND stock_quantity >= quantity_box) THEN
                UPDATE inventory
                SET stock_quantity = stock_quantity - quantity_box,
                    last_update = NOW()
                WHERE warehouse_id = NEW.warehouse_id AND product_id = NEW.product_id;
            ELSE
                RAISE EXCEPTION 'Stock insuficiente para el producto % en el almacén %.', NEW.product_id, NEW.warehouse_id;
            END IF;
        END IF;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger que se ejecuta DESPUÉS de insertar en la tabla inventory_movements
CREATE TRIGGER inventory_update_trigger
AFTER INSERT ON inventory_movements
FOR EACH ROW
EXECUTE PROCEDURE calculate_box_quantity();
